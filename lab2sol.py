x = 7 #here 7 is assigned to variable x
print('value of x is ',x) #the value of x is printed

x = float(x) #here float value of x is assigned to x
print('float value of x is', x) #the float value of x is printed

y = 6.756 #here 6.756 is assigned to y
print('value of y is', y) #here the value of y is printed

z = y**x #here value of y is assigned as y*x
print('value of z is',round(z)) #rounded value of z is printed


result = x/y #value of x/y is assigned to result
